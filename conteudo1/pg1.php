<div class="titulo">
- Por que há maior incidência de casos em meninos?
</div>
<br>
<div class="corpo">
A frequência de autismo em meninos é quatro vezes maior que em meninas, de acordo com a Organização Mundial de Saúde (OMS). Um estudo mais recente dos cientistas dos Institutos Nacionais da Saúde dos Estados Unidos (NIH, em inglês) descobriram que uma única alteração de aminoácido no gene NLGN4 pode causar uma diferença em alguns casos, também descobriram que a região em torno desse aminoácido no NLGN4X é sensível a mutações na população humana. 
 </div>
 <div class="corpo">
 Pesquisadores liderados por Katherine Roche, neurocientista do Ninds, compararam dois genes NLGN4 (um no cromossomo X e outro no cromossomo Y), que são importantes para estabelecer e manter sinapses, os pontos de comunicação entre os neurônios.
 </div>
 <div class="corpo">
 Cada célula do nosso corpo contém dois cromossomos sexuais. As meninas carregam duas cópias do cromossomo X; ou seja, elas têm um “backup”. Os meninos, no entanto, têm apenas um X de suas mães e o Y de seus pais. Isso os deixaria mais expostos às consequências dessa herança genética autista.
 </div>
 <br>
 <div class="fonte">
Fontes:
