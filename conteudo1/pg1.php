<div class="titulo">
- Caracteristicas
</div>
<br>
<div class="corpo">
Alguns sinais a serem observados a partir de um ano de idade são:
 </div>
 <ul>
<li>Perder habilidades já adquiridas, como balbucio ou gesto dêitico de alcançar, contato ocular ou sorriso social;</li>
<li>Não se voltar para sons, ruídos e vozes no ambiente;</li>
<li>Não apresentar sorriso social;</li>
<li>Baixo contato ocular e deficiência no olharsustentado;</li>
<li>Baixa atenção à face humana (preferência por objetos);</li>
<li>Demonstrar maior interesse por objetos do que por pessoas;</li>
<li>Não seguir objetos e pessoas próximos em movimento;</li>
<li>Apresentar pouca ou nenhuma vocalização;</li>
<li>Não aceitar o toque;</li>
<li>Não responder ao nome;</li>
<li>Imitação pobre;</li>
<li>Baixa frequência de sorriso e reciprocidade social, bem como restrito engajamento social (pouca iniciativa e baixa disponibilidade de resposta)</li>
<li>Interesses não usuais, como fixação em estímulos sensório-viso-motores;</li>
<li>Incômodo incomum com sons altos;</li>
<li>Distúrbio de sono moderado ou grave;</li>
<li>Irritabilidade no colo e pouca responsividade no momento da amamentação.</li>
</ul>
<div class="corpo">
A Academia Americana de Pediatria e a Sociedade Brasileira de Pediatria (SBP), recomenda que toda criança seja triada entre 18 e 24 meses de idade para o TEA, mesmo que não tenha sinais clínicos claros e evidentes deste diagnóstico ou de outros atrasos do desenvolvimento. 
 </div>
