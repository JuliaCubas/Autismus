<div class="titulo">
- Classificações:
</div>
<br>
<ul>
<div class="corpo">
Atualmente o autismo é identificado por três graus: o leve, o moderado e o severo, também definidos como nível 1 (leve), nível 2 (moderado) e nível 3 (severo). Essas condições agora são chamadas de Transtorno do Espectro Autista. 
</div>
<br>
 <li>
 O grau leve ou nível 1 consiste em pouco déficit nas áreas da comunicação, socialização e no seu comportamento em geral.
</li>
 <div class="corpo">
 Exemplo: a criança que aprende bem na escola e não teve atraso na fala, mas apresenta problemas de comportamento, comunicação (só fala sobre o que gosta; tem problema para se comunicar) e dificuldades de interagir com outras pessoas.
</div>
<br>
 <li>
 O grau moderado (médio) ou nível 2 consiste em um déficit moderado nas áreas da comunicação, socialização e comportamento.
 </li>
 <div class="corpo">
 Exemplo: criança gosta de participar de brincadeiras mas não entender a brincadeira; tem dificuldade de compreender regras sociais; se comunica através de poucas palavras ou não apresenta linguagem verbal.
 </div>
 <br>
 <li>
 O grau severo (grave) ou nível três consiste no déficit grave de comunicação, socialização e comportamento
 </li>
 <div class="corpo">
 Exemplo: a criança ignora comandos, pode ter episódios de agressividade, grave atraso/ausência de linguagem verbal e severas dificuldades no modo de interagir e no seu comportamento, principalmente, na companhia de outros. 
 </div>
 <br>
</ul>
