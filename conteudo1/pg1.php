<div class="titulo">
- Possíveis causas:
</div>
<br>
<div class="corpo">
Genéticos: Causado por uma combinação de fatores genéticos e ambientais. Hoje, os especialistas consideram que a contribuição dos fatores genéticos esteja ao redor de 90%. Aproximadamente 10-15% dos casos têm um específico, uma causa genética identificável e outras desordens congenitais, tais como como a síndrome frágil de X, esclerose tuberosa, e síndrome de Angelman.
</div>
 <div class="corpo">
Ambientais: A exposição aos agentes ambientais tais como agentes infecciosos ou agentes químicos (incluindo medicamentações e toxinas ambientais) durante a gravidez. 
 </div>
 <div class="corpo">
O TEA é também frequentemente associado a outros transtornos psiquiátricos (transtorno do déficit de atenção e hiperatividade, depressão e ansiedade) e a outras condições médicas (epilepsia; transtornos genéticos).
 </div>
