<div class="titulo">
- O que é?
</div>
<br>
<div class="corpo">
 O T.E.A (Transtorno do Espectro Autista) é um transtorno do desenvolvimento neurológico, também chamado de Desordens do Espectro Autista (DEA ou ASD em inglês), recebe o nome de espectro, porque envolve situações e apresentações diversificadas, numa gradação. Trata-se de um transtorno permanente, que não há cura, ainda que a intervenção precoce possa alterar o prognóstico e suavizar os sintomas. Embora que em grande parte só possa ser identificado entre os 2 e 3 anos de idade, há de se notar, em alguns casos, por volta dos 18 meses. 
</div>
<br><br><br>

