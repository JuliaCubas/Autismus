<div class="titulo">
- Diagnostico: 
</div>
<br>
<div class="corpo">
O diagnóstico do espectro autista é apenas clínico, geralmente realizado por neurologistas ou psiquiatras. Isso significa que, para certificar se uma pessoa é autista, é preciso observar o comportamento do paciente e analisar informações coletadas com as pessoas que convivem com ele, com o auxílio de questionários protocolados, como a escala MCHAT, por exemplo. 
 </div>
 <div class="corpo">
 Há dois manuais utilizados para diagnosticar o autismo: 
 </div>
 <div class="corpo">
 São o CID e o DSM. Também existem algumas escalas padronizadas para o diagnóstico e rastreio de autismo, mas elas somente devem ser utilizadas por profissionais treinados e capacitados para isso.
  </div>
   <div class="corpo">
O CID-10 é o critério adotado no Brasil pelo Sistema Único de Saúde (SUS). Ele abrange todas as doenças, incluindo os transtornos mentais, e foi elaborado pela Organização Mundial de Saúde (OMS). CID-10 significa “Classificação Internacional de Doenças”, e o número 10 indica a versão, ou seja, já foram realizadas 10 atualizações e revisões desse código.

 </div>
  <div class="corpo">
O DSM-5 (Diagnostic and Statistical Manual) abrange apenas os transtornos mentais e tem sido mais utilizado em ambientes de pesquisa, porque possui itens mais detalhados, em forma de tópicos. Foi elaborado pela Associação Americana de Psiquiatria.
 </div>
