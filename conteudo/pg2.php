<div class="titulo">
- Tratamento: 
</div>
<br>
<div class="corpo">
Não há cura para o autismo, mas o reconhecimento precoce pode reduzir os sintomas, além de oferecer um pilar de apoio ao desenvolvimento e à aprendizagem. A variedade de terapias, voltadas para o tratamento do autismo, se deve às diversas características que apresentam e à grande diferenciação na apresentação dos casos, cada uma atende a uma necessidade específica. O melhor resultado não é obtido pela frequência de todas as terapias disponíveis. De nada adianta sobrecarregaremos os autistas com uma maratona de tratamentos. Os êxitos virão na medida em que se puder conciliar as necessidades do autista com as de sua família, sejam necessidades físicas, afetivas, sociais e financeiras. Dentre os tratamentos, os essenciais são com: médicos, fonoaudiólogos, fisioterapeutas, terapeutas ocupacionais, psicólogos, pedagogos e professores. 
</div>
